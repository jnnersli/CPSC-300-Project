		<h2>The Judging</h2>
		<p>Our application seeks to make the process of judging debates as simple as possible. Gone are the days of entering scores using pen and paper. Gone are the days of (God forbid) adding up the scores yourself.</p>
		<p>This application allows you to create an event that other judges can connect to and contribute to. Team registration, pairing, and scoring can all be done in one place. People can connect using any device that has an internet browser.</p>
		
		<h2>Report Cards</h2>
		<p>We have a feature that allows individual report cards to be generated for the students. Included in these will be their scores for each round, comments from judges, and a total score breakdown.</p>
		
